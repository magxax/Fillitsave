#include "libft.h"

int	ft_atoi(const char *str)
{
	int	c;
	int	i;
	int	a;

	c = 0;
	i = 0;
	a = 1;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v' ||
			str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			a = -1;
		i++;
	}
	while (ft_isdigit(str[i]) && str[i])
	{
		c = c * 10 + str[i] - '0';
		i++;
	}
	return (a * c);
}
