#include "libft.h"

char		*ft_itoa(int n)
{
	int		b;
	int		i;
	char	*str;

	i = 2;
	b = n;
	while ((b = b / 10))
		i++;
	if (n < 0)
	{
		b = 1;
		i++;
	}
	str = (char *)malloc((i) * sizeof(char));
	i--;
	str[i] = '\0';
	while (i--)
	{
		str[i] = ft_abs(n % 10) + '0';
		n = n / 10;
	}
	if (b == 1)
		str[0] = '-';
	return (str);
}
